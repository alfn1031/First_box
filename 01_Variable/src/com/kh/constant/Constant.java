package com.kh.constant;

public class Constant {
	// * 상수
	// 수학익힘책 -> 변하지 않는 값
	public static void main (String[] args) {
		// 정수 (+,-)
		System.out.println(7);
		System.out.println(-7);
		System.out.println("-------------");
		//실수(n.nnn)
		System.out.println(7.7);
		System.out.println(-7.7);
		System.out.println("-------------");
		// (단일)문자, 문자열 두가지로 구분
		System.out.println('돈');
		System.out.println("돈 100억");
		// '는 한글자 "는 2글자 이상
		// 논리값 (true, false)
		System.out.println(true);
		System.out.println(false);
		
		
		System.out.println("----");
		System.out.println(7);           //7
		System.out.println('7');         //칠
		System.out.println("7");         
		//위의 7들은 모두 같아보이지만 컴퓨터에게는 다르게 보임
		
		
		
		
	}

}
